/**
 * Literals are used to initialize variables, set parameters, and provide values directly in the code. 
 * A literal is a fixed value that appears directly in the code
 */
// Usage in Code of Literals

const PI = 3.14159; // Number Literal
const message = "Welcome To JavaScript!"; // String Literal
const isCompleted = true; // Boolean Literal
const personInfo = { // Object Literal
  name: "Smith",
  age: 40,
  city: "USA",
};
const item = null; // Null Literal
const cars = ["Saab", "Volvo", "BMW"]; // Array literal
const regex = /\d{2}/; // Regular Expression Literals
const Name = "Alice";
const greeting = `Hello, ${Name}!`; // ` ` is a template literal